#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int ret;

	ret = 0;
	(!s1 && !s2) ? (ret = 1) : (0);
	(s1 && s2) ? (ret = ft_strncmp(s1, s2, n)) : (0);
	if (ret != 0)
		return (0);
	return (1);
}
